// Підключення бібліотеки <iostream> для забезпечення операцій вводу/виводу (наприклад, cout для виведення тексту).
#include <iostream>

// Підключення бібліотеки <cmath> для використання математичних функцій, зокрема sqrt() для обчислення квадратного кореня.
#include <cmath>

// Використання простору імен std, що дозволяє уникнути попереднього написання "std::" перед стандартними об'єктами (наприклад, cout, cin).
using namespace std;

// Функція isPrime перевіряє, чи є задане число простим.
// Просте число – це натуральне число, яке більше за 1 і має рівно два дільники: 1 та саме число.
bool isPrime(int num) {
    // Перевірка: якщо число менше за 2, то воно не може бути простим, адже за визначенням простими є числа, більші або рівні 2.
    if (num < 2)
        // Якщо умова істинна, повертаємо false, що означає "не просте число".
        return false;

    // Для ефективної перевірки простоти достатньо перевіряти дільники від 2 до квадратного кореня з num.
    // Ідея полягає в тому, що якщо num має дільник більший за квадратний корінь, то обов'язково існує і дільник, менший за квадратний корінь.
    // Обчислюємо квадратний корінь з num за допомогою функції sqrt(num) з бібліотеки <cmath>.
    // Приводимо результат до цілочисельного типу для використання в циклі.
    for (int i = 2; i <= sqrt(num); i++) {
        // Перевірка: якщо число num ділиться на число i без остачі (залишок від ділення дорівнює 0),
        // тоді i є дільником num, що означає, що num має додатковий дільник, окрім 1 і самого num.
        if (num % i == 0)
            // Якщо знайдено хоча б один дільник, повертаємо false, тобто число не є простим.
            return false;
    }

    // Якщо цикл завершився без виявлення дільників, це означає, що num має лише два дільники: 1 і саме число.
    // Отже, повертаємо true, що свідчить про простоту числа.
    return true;
}

// Функція main є точкою входу в програму.
// Звідси починається виконання програми.
int main(void) {
    // Цикл for перебирає всі цілі числа від 0 до 21 включно.
    // Змінна i представляє поточне число, яке перевіряється на простоту.
    for (int i = 0; i <= 21; i++) {
        // Викликаємо функцію isPrime для перевірки, чи є поточне число простим.
        // Якщо функція повертає true, це означає, що число є простим.
        if (isPrime(i))
            // Якщо число просте, виводимо його у консоль за допомогою cout, додаючи пробіл після числа для розділення.
            cout << i << " ";
    }
    
    // Після завершення циклу виводимо символ кінця рядка (endl), щоб курсор перейшов на новий рядок.
    cout << endl;
    
    // Повертаємо 0, що свідчить про успішне завершення роботи програми.
    return 0;
}
